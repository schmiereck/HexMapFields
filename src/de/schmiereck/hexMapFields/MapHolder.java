/**
 * <p>
 *	{@link Map} Holder.
 * </p>
 * 
 * @author thoma
 * @version <p>28.06.2017:	created, thoma</p>
 */
public class MapHolder
{
	//**********************************************************************************************
	// Fields:
	
	private Map map;
	
	//**********************************************************************************************
	// Functions:
	
	/**
	 * @return 
	 *			the value of attribute {@link #map}.
	 */
	public Map getMap()
	{
		return this.map;
	}

	/**
	 * @param map 
	 * 			used to set the value of attribute {@link #map}.
	 */
	public void setMap(Map map)
	{
		this.map = map;
	}

}
